package Operators;

public class RelationalOperator {
    public static void main(String[] args) {
        int a=12,b=45;
         System.out.println(a==b);
         System.out.println(a!=b);
         System.out.println(a>=b);
         System.out.println(a<=b);
         System.out.println(a>b);
         System.out.println(a<b);
    }
}
