package Operators;

public class UnaryOperator {
    public static void main(String[] args) {
                 int num=5,n=5;
         //Increament Operator
         System.out.println(++num);
         System.out.println(n++);


         //Decreament operator
         System.out.println(--num);
         System.out.println(n--);

    }
}
